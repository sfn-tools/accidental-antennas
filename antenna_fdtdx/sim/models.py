"""Model configurations for FDTDX antenna runs."""

from __future__ import annotations

from dataclasses import dataclass, replace
from typing import Dict, List

from .model_defaults import COMMON_DEFAULTS, S15_WEDGE_DEFAULTS


@dataclass(frozen=True)
class ModelConfig:
    name: str
    f_low_hz: float
    f_high_hz: float
    f0_hz: float
    sweep_low_hz: float
    sweep_high_hz: float
    sweep_points: int
    slice_angle_deg: float
    inner_radius_mm: float
    outer_radius_mm: float
    substrate_thickness_mm: float
    copper_thickness_mm: float
    feed_offset_mm: float
    feed_length_mm: float
    feed_width_mm: float
    source_offset_mm: float
    feed_stub_length_mm: float
    design_length_mm: float
    design_width_mm: float
    air_margin_mm: float
    base_resolution_mm: float
    source_gap_mm: float
    port_width_mm: float
    port_air_above_mm: float
    port_air_below_mm: float
    port_offset_mm: float
    monitor_margin_mm: float
    min_trace_mm: float
    min_gap_mm: float
    eta_min: float
    projection_beta: float
    ground_trim_mm: float = 0.0

    def band_freqs(self, points: int = 7) -> List[float]:
        if points < 2:
            return [self.f0_hz]
        step = (self.f_high_hz - self.f_low_hz) / (points - 1)
        return [self.f_low_hz + i * step for i in range(points)]

    def sweep_freqs(self, points: int | None = None) -> List[float]:
        pts = self.sweep_points if points is None else points
        if pts < 2:
            return [0.5 * (self.sweep_low_hz + self.sweep_high_hz)]
        step = (self.sweep_high_hz - self.sweep_low_hz) / (pts - 1)
        return [self.sweep_low_hz + i * step for i in range(pts)]


def _model(**kwargs) -> ModelConfig:
    data = dict(COMMON_DEFAULTS)
    data.update(kwargs)
    return ModelConfig(**data)


def _model_s15(**kwargs) -> ModelConfig:
    data = dict(COMMON_DEFAULTS)
    data.update(S15_WEDGE_DEFAULTS)
    base_resolution_mm = float(kwargs.pop("base_resolution_mm", 0.3))
    base_resolution_mm = max(base_resolution_mm, 0.3)
    data.update(kwargs)
    data["base_resolution_mm"] = base_resolution_mm
    return ModelConfig(**data)


def effective_source_gap_mm(model: ModelConfig, resolution_mm: float) -> float:
    min_gap = max(model.min_gap_mm, 4.0 * resolution_mm)
    return max(model.source_gap_mm, min_gap)


def adjust_model_for_resolution(model: ModelConfig, resolution_mm: float) -> ModelConfig:
    gap_mm = effective_source_gap_mm(model, resolution_mm)
    if gap_mm <= model.source_gap_mm:
        return model
    return replace(model, source_gap_mm=gap_mm)


